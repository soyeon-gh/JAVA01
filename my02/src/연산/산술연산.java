package 연산;

public class 산술연산 {

	public static void main(String[] args) {
		
		int n1 = 200; // int : -21억 ~ 21억
		int n2 = 100;
		
		int n3 = n1 + n2;
		System.out.println("더하기 : n1 + n2 = " + n3);
		int n4 = n1 - n2;
		System.out.println("빼   기 : n1 - n2 = " + n4);
		int n5 = n1 * n2;
		System.out.println("곱하기 : n1 * n2 = " + n5);
		int n6 = n1 / n2;
		System.out.println("나누기 : n1 / n2 = " + n6);
		int n7 = n1 % n2;
		System.out.println("나머지 : n1 % n2 = " + n7);
		
		
	}

}
