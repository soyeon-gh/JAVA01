package 데이터;

public class 기본데이터2 {

	public static void main(String[] args) {
		// 기본데이터 저장
		// 정수, 실수 저장
		
		int x = 250; // 정수 : -21억 ~ 21억
		double y = 5.5; // 실수
		
		char z = '한'; // 한 글자 문자 ''
		boolean p = true; // false
		
		System.out.println( "x는 " + x);
		System.out.println( "y는 " + y);
		System.out.println( "p는 " + p);
		System.out.println( "z는 " + z);
		
	}

}
